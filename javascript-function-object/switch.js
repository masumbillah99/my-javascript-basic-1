// if-else statement
const color = '';
if (color === 'green') {
    console.log('you are a green man');
}
else if (color === 'blue') {
    console.log('you are a blue man');
}
else if (color === 'red') {
    console.log('you are a red man');
}
else if (color === 'white') {
    console.log('you are a white man');
}
else if (color === 'yellow') {
    console.log('you are a yellow man');
}
else {
    console.log('you are a black man');
}

// switch statement (awesome)
switch (color) {
    case 'green':
        console.log('you are a green man');
        break;
    case 'blue':
        console.log('you are a blue man');
        break;
    case 'white':
        console.log('you are a white man');
        break;
    case 'red':
        console.log('you are a red man');
        break;
    case 'yellow':
        console.log('you are a yellow man');
        break;
    default:
        console.log('you are a kala / black man');
}